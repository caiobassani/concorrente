    public void iniciar() {
        //Cria a conta com saldo inicial de R$10000,00
        Conta conta = new Conta(10, "Caio", 500);
        //Cria as Threads que irão consumir o recurso da conta
        Saque AGastadora = new Saque("AGastadora", 3000, 10, conta);
        Saque AEsperta = new Saque("AEsperta", 6000, 50, conta);
        Saque AEconomica = new Saque("AEconomica", 12000, 5, conta);
        //Cria a Thread que irá produzir o recurso da conta
        Deposito APatrocinadora = new Deposito("APatrocinadora", conta);       
        
        //Coloca as Threads para rodar
        AGastadora.start();               
        AEsperta.start();               
        AEconomica.start();
        
        APatrocinadora.start();
        
        this.jButton1.setEnabled(false);
    }
    
